BasicUpstart2(start)
        * = $4000 "Main Program"
start:  lda #$07
		sta $d020	// set background to 7
		lda #$ff
		sta $d015  	// turn on all sprites
		lda #$0d    // 13 * 64 -> 832  (D h -> 13 d)
		sta $07f8	// set sprite data location ($07f8 h -> 2040 d)

		// set sprite 0 pos x,y
		lda #$64
		sta $d000	// sprite 0 x
		lda #$64
		sta $d001 	// sprite 0 y

		lda #$ff
		sta $0800
loop:	inc $d000
		lda #$ff
		sta $0801		
inner_loop:
        nop
		dec $0801
		lda $0801
		cmp #$00
		bne inner_loop
		dec $0800
		lda $0800
		cmp #$00
		bne loop
		jmp start
